package JavaExamples.DilayWorking;

public class SwitchCase {
    static void main() {

        int day = 3;

        switch (day){

            case 1:
                System.out.println(" Pazartesi ");
                break;

            case 2:
                System.out.println(" Salı ");
                break;

            case 3:
                System.out.println(" Çarşamba");
                break;

            default:
                System.out.println(" hatalı giriş ");
                break;



        }

    }
}
